def tablespoons_to_100ml(tbsp):
    return (tbsp*15) / 100


def teaspoon_to_100ml(tsp):
    return (tsp*15) / 100


def cups_to_100ml(cups):
    return (cups*225) / 100


def pound_to_100g(pounds):
    return (pounds*450) / 100


def ounce_to_100g(ounces):
    return (ounces*30) / 100


